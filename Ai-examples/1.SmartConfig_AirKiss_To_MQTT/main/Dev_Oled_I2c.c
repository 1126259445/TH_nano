// 向OLED写入一字节数据/指令
//---------------------------------------------------------------
static void  OLED_WR_Byte(uint8_t OLED_Byte, uint8_t OLED_Type)
{
	if(OLED_Type)
	OLED_Write_Data(OLED_Byte);		// 写入数据
	else
	OLED_Write_Command(OLED_Byte);// 写入指令
}
