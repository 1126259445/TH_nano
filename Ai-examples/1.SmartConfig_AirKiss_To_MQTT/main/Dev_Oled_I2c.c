// 在指定坐标起始处显示字符串
//-------------------------------------------------------------------
void  OLED_ShowString(uint8_t x, uint8_t y, char * Show_char,uint8_t fount_size)
{
	uint8_t N_Char = 0;		// 字符序号

	if(fount_size == SIZE16)
	{
		while (Show_char[N_Char]!='\0')// 如果不是最后一个字符
		{
			OLED_ShowChar(x,y,Show_char[N_Char],fount_size); 	// 显示一个字符

			x += 8;// 列数加8，一个字符占8列

			if(x>=128) {x=0;y+=2;} 	// 当x>=128，另起一页

			if(y >= 8) y = 0; //当y >8 从头开始
			
			N_Char++;// 指向下一个字符
		}
	}
	else if(fount_size == SIZE32)
	{
		while (Show_char[N_Char]!='\0')// 如果不是最后一个字符
		{
			OLED_ShowChar(x,y,Show_char[N_Char],fount_size); 	// 显示一个字符

			x += 16;// 列数加8，一个字符占16列

			if(x>=128) {x=0;y+=4;} 	// 当x>=128，另起一页

			if(y >= 8) y = 0; //当y >8 从头开始
			
			N_Char++;// 指向下一个字符
		}
	}
}
